    public void serviceRequest(String name)
    {
        String query="Select * from clothes where name='" + name + "'";
        
        currentItem=name;
        isEdited=true;
        
        
        db.openConnection();
        
        try
        {
            ResultSet rs= db.read(query);
            rs.next();
            
            txtItemName.setText(rs.getString("name"));
            txtPrice.setText(rs.getString("price"));
            txtCount.setText(rs.getString("countVal"));
            
            
        }
        catch(Exception e)
        {
                JOptionPane.showMessageDialog(null, e.getMessage(),"Oops",JOptionPane.ERROR_MESSAGE);
        }
        
        db.closeConnection();
       
    }
    
