    public void clearAll()
    {
        isEdited=false;
        currentItem="";
        txtPrice.setText(currentItem);
        txtCount.setText(currentItem);
        txtItemName.setText(currentItem);
    }
    private boolean verified()
    {
        
        String problems="";
        int myVal;
        
        if(txtItemName.getText().length()==0 ||  txtItemName.getText().length()> 30)
        {
            problems="Item Name Should be between 1 to 30 characters";
        }
       
       try
                {
                 myVal=   Integer.parseInt(txtPrice.getText());
                 if(myVal<=0 || txtPrice.getText().length()==0) throw new Exception("Problem");
                }
       catch(Exception e)
                {
                        problems="Invalid Price given";
                }
       
       
       try
                {
                 myVal=   Integer.parseInt(txtCount.getText());
                 if(myVal<=0 || txtCount.getText().length()==0) throw new Exception("Problem");
                 
                }
       catch(Exception e)
                {
                        problems="Invalid Count Value given";
                }
       
       
        
        if(problems.length()!=0)
        {
            JOptionPane.showMessageDialog(null, problems);
            return false;
        }
        
        return true;
        
    }
    
    boolean updateToDb()
    {
        
        try
        {
            if(isEdited)
            {
                ItemType.updateItem(txtItemName.getText(), txtPrice.getText(), txtCount.getText(),currentItem);
            }
            else
            {
                 ItemType.addItem(txtItemName.getText(), txtPrice.getText(), txtCount.getText());
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e.getMessage(),"Oops",JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        return true;
            
    }
    
